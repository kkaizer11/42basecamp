#include <unistd.h>

void	put_str(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		write(1, &str[i++], 1);
}

int	main(int argc, char *argv[])
{
	if (argc > 0)
	{
		put_str(argv[0]);
		put_str("\n");
	}
	return (0);
}
